    void DangNhap(){
        String manv = txtTaikhoan.getText().trim();
        String makh = txtTaikhoan.getText().trim();
        String matkhaunv = new String(txtMatkhau.getPassword()).trim();
        String matkhaukh = new String(txtMatkhau.getPassword()).trim();
        NhanVienDao daonv = new NhanVienDao();
        KhachHangDao daokh = new KhachHangDao();
        NhanVien nv = daonv.selectById(manv);
        KhachHang kh = daokh.selectById(makh);
        if (nv==null && kh==null) {
        MsgBox.alert(this,"Sai tài khoản đăng nhập");
        }else if(nv !=null && !matkhaunv.equals(nv.getMatKhau())){
            MsgBox.alert(this, "Sai Mật Khẩu !");
        }else if(kh !=null && !matkhaukh.equals(kh.getMatKhau())){
            MsgBox.alert(this, "Sai Mật Khẩu !");
        }
        else{
                Auth.user1 = nv;
                Auth.user2 = kh;
                this.dispose();
                MsgBox.alert(this,"Đăng nhập thành công" );
        }
    }
    void Ketthuc(){
        if (MsgBox.confirm(this, "Bạn có muốn kết thúc ứng dụng ???")) {
            System.exit(0);
        }
    }
    void check (){
        String ID = txtTaikhoan.getText();
        String Pass = new String (txtMatkhau.getPassword());
        if (ID.length()==0) {
            MsgBox.alert(this, "Bạn chưa có tài khoản đăng nhập");
            check=0;
            return;
        }else if (Pass.length()==0) {
            MsgBox.alert(this, "Bạn chưa có mật khẩu");
            check=0;
            return;
        }else {
            check=1;
        }
    }
